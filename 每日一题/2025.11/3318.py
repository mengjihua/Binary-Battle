class Solution:
    def findXSum(self, nums: List[int], k: int, x: int) -> List[int]:
        n = len(nums)
        ans = []
        for i in range(n - k + 1):
            sub = nums[i:i + k]
            st = set(sub)
            if len(st) < x:
                ans.append(sum(sub))
            else:
                cnt = sorted(Counter(sub).items(), key=lambda x: (x[0], x[1]), reverse=True)
                ans.append(sum(c * v for c, v in cnt[:x]))
        return ans