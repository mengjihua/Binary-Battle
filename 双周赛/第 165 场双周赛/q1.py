class Solution:
    def smallestAbsent(self, nums: List[int]) -> int:
        avg = sum(nums) / len(nums)
        start = max(1, int(avg) + 1)
        set_nums = set(nums)
        for i in range(start, max(nums) + 2):
            if i not in set_nums:
                return i
        return 1